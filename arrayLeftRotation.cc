vector<int> array_left_rotation(vector<int> a, int n, int k) {
    if (k >= n) { // if the number of rotations is bigger than the size of array
        k = k % n;
    }
    
    int newIndex = 0;
    vector<int> b(n);
    b = a;
    for (int i = 0; i < n; ++i) {
        newIndex = i - k;
        if (newIndex < 0) {
            newIndex += n;
        }
        b[newIndex] = a[i];
    }
    a = b;
    return a;
}

int main(){
    int n;
    int k;
    cin >> n >> k;
    vector<int> a(n);
    for(int a_i = 0;a_i < n;a_i++){
        cin >> a[a_i];
    }
    vector<int> output = array_left_rotation(a, n, k);
    for(int i = 0; i < n;i++)
        cout << output[i] << " ";
    cout << endl;
    return 0;
}

